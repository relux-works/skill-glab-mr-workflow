class BotFilteringTests(unittest.TestCase):
    def test_get_unresolved_discussions_filters_bot_noise_by_default(self) -> None:
        discussions = [
            {
                "id": "bot-only",
                "resolved": False,
                "individual_note": False,
                "notes": [
                    {
                        "id": 1,
                        "system": False,
                        "resolvable": True,
                        "resolved": False,
                        "body": "Generated by Danger Swift",
                        "author": {"username": "group_123_bot_deadbeef"},
                    }
                ],
            },
            {
                "id": "human",
                "resolved": False,
                "individual_note": False,
                "notes": [
                    {
                        "id": 2,
                        "system": False,
                        "resolvable": True,
                        "resolved": False,
                        "body": "Please rename this API before merge.",
                        "author": {"username": "alice"},
                    }
                ],
            },
        ]
        with mock.patch.object(gmr_main, "glab_json", return_value=discussions):
            filtered = gmr_main.get_unresolved_discussions({"hostname": "gitlab.example.com", "repo_encoded": "x", "iid": "1"})
            full = gmr_main.get_unresolved_discussions(
                {"hostname": "gitlab.example.com", "repo_encoded": "x", "iid": "1"},
                include_bots=True,
            )

        self.assertEqual([item["id"] for item in filtered], ["human"])
        self.assertEqual([item["id"] for item in full], ["bot-only", "human"])


