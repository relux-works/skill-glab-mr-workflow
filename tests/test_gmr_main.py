class ResolveMrTargetTests(unittest.TestCase):
    def test_resolve_mr_target_from_url(self) -> None:
        resolved = gmr_main.resolve_mr_target(
            "https://gitlab.example.com/group/sub/project/-/merge_requests/42",
            None,
            None,
        )

        self.assertEqual(resolved["hostname"], "gitlab.example.com")
        self.assertEqual(resolved["repo"], "group/sub/project")
        self.assertEqual(resolved["iid"], "42")
        self.assertEqual(resolved["repo_encoded"], "group%2Fsub%2Fproject")

    def test_resolve_mr_target_uses_inferred_repo_context_for_iid(self) -> None:
        with mock.patch.object(
            gmr_main,
            "infer_repo_context",
            return_value=("gitlab.example.com", "group/sub/project"),
        ):
            resolved = gmr_main.resolve_mr_target("42", None, None)

        self.assertEqual(resolved["hostname"], "gitlab.example.com")
        self.assertEqual(resolved["repo"], "group/sub/project")
        self.assertEqual(resolved["mr_url"], "https://gitlab.example.com/group/sub/project/-/merge_requests/42")


class TraceExtractionTests(unittest.TestCase):
    def test_extract_failure_excerpt_prefers_error_lines_and_strips_noise(self) -> None:
        trace = """
section_start:123:foo
\x1b[31mERROR: Job failed: exit status 1\x1b[0m
$ echo -e "section_end:123:foo"
[!] Codeowners consistency check failed with error:
[NO_OWNERS] No owners found for changed file AGENTS.md
"""

        excerpt = gmr_main.extract_failure_excerpt(trace, 4)

        self.assertEqual(
            excerpt,
            [
                "[!] Codeowners consistency check failed with error:",
                "[NO_OWNERS] No owners found for changed file AGENTS.md",
            ],
        )

    def test_extract_failure_excerpt_prefers_semantic_failure_over_shell_and_generic_exit(self) -> None:
        trace = """
$ if ! xcode-select -p >/dev/null 2>&1; then sudo xcode-select -s /Applications/Xcode.app/Contents/Developer; fi
xcode-select: note: No developer tools were found, requesting install.
Generated by Danger Swift
Danger: Merge request description does not match the required template.
Please update the MR description before merge.
ERROR: Job failed: exit status 1
"""

        excerpt = gmr_main.extract_failure_excerpt(trace, 4)

        self.assertEqual(
            excerpt,
            [
                "Danger: Merge request description does not match the required template.",
                "Please update the MR description before merge.",
            ],
        )


