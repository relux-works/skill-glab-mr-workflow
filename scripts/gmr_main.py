def command_mr_merge(args: argparse.Namespace) -> None:
    ctx = resolve_mr_target(args.target, args.repo, args.hostname)
    mr = get_mr(ctx)
    sha = args.sha or mr.get("sha")
    if not sha:
        raise CommandError("Could not determine the merge request HEAD SHA for merge.")
    if mr.get("draft"):
        raise CommandError("Refusing to merge a draft merge request.")

    head_pipeline = mr.get("head_pipeline") or {}
    pipeline_status = head_pipeline.get("status")
    if pipeline_status == "failed":
        raise CommandError("Refusing to merge while the head pipeline is failed.")
    if pipeline_status in {"running", "pending", "created", "preparing", "waiting_for_resource"} and not args.auto_merge:
        raise CommandError(
            "Head pipeline is not finished yet. Re-run with --auto-merge or wait for a green pipeline."
        )

    command = [
        "glab",
        "mr",
        "merge",
        ctx["iid"],
        "-R",
        ctx["repo"],
        "--sha",
        sha,
        "--yes",
    ]
    if not args.keep_source_branch:
        command.append("--remove-source-branch")
    if args.rebase:
        command.append("--rebase")
    if args.squash:
        command.append("--squash")
    if args.auto_merge:
        command.append("--auto-merge")
    if args.message:
        command += ["--message", args.message]

    output = run_command(command, env=glab_cli_env(ctx["hostname"]))
    print_json(
        {
            "action": "merge",
            "target": ctx,
            "sha": sha,
            "auto_merge": args.auto_merge,
            "removed_source_branch": not args.keep_source_branch,
            "output": output.strip() or None,
        }
    )


