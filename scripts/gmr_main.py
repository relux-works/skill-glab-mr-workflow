    list_parser.add_argument(
        "--state",
        choices=["opened", "closed", "merged", "all"],
        default="opened",
    )
    list_parser.add_argument("--author")
    list_parser.add_argument("--assignee")
    list_parser.add_argument("--reviewer")
    list_parser.add_argument("--mine", action="store_true")
    list_parser.add_argument(
        "--mine-role",
        choices=["author", "assignee", "reviewer"],
        default="author",
    )
    list_parser.add_argument("--draft", action="store_true")
