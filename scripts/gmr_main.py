# CLI/API split is deliberate:
# - Read operations (get_mr, pipelines, jobs, traces, discussions) use `glab api`
#   for structured JSON responses with explicit hostname control.
# - Write and list operations (mr list, create, approve, merge) use `glab mr` CLI
#   subcommands for built-in repo resolution, flag composition, and auth handling.

ANSI_ESCAPE_RE = re.compile(r"\x1B\[[0-?]*[ -/]*[@-~]")
MR_URL_RE = re.compile(
    r"^(?:(?P<scheme>https?)://)?(?P<host>[^/]+)/(?P<repo>.+)/-/merge_requests/(?P<iid>\d+)/?$"
)
ERROR_LINE_RE = re.compile(
    r"(\[!\]|\berror\b|\bfailed\b|\bexception\b|\btraceback\b|\bfatal\b|\bno owners found\b|\bnot found\b)",
    re.IGNORECASE,
)
GENERIC_JOB_FAILURE_RE = re.compile(r"^ERROR:\s+Job failed:\s+exit status\s+\d+\s*$", re.IGNORECASE)
SHELL_COMMAND_RE = re.compile(r"^\$\s+")
SHELL_IF_COMMAND_RE = re.compile(r"^\$\s+if\s+!", re.IGNORECASE)
SEMANTIC_FAILURE_RE = re.compile(
    r"(\bdanger\b|\bfastlane\b|\bxcodebuild\b|\bswiftlint\b|\beslint\b|\bktlint\b|\bdetekt\b|"
    r"\bclang-tidy\b|\bcodeowners\b|\btemplate\b|\bdescription\b|\blint\b|\btest(?:s)?\b)",
    re.IGNORECASE,
)
BOT_AUTHOR_RE = re.compile(r"(^|[_-])bot([_-]|$)", re.IGNORECASE)
MR_WEB_URL_RE = re.compile(r"https?://\S+/-/merge_requests/\d+")
BOT_BODY_MARKERS = (
    "generated by",
    "danger swift",
    "a review request will be placed here",
    "metadata=\"generated_by_",
    "danger-id-",
)
MINE_ALIASES = {"@me", "me", "mine", "@mine"}


class CommandError(RuntimeError):
    pass


def run_command(
    args: list[str],
    *,
    capture_output: bool = True,
    env: dict[str, str] | None = None,
    input_text: str | None = None,
) -> str:
    completed = subprocess.run(
        args,
        text=True,
        input=input_text,
        capture_output=capture_output,
        env=env,
    )
    if completed.returncode != 0:
        stderr = (completed.stderr or "").strip()
        stdout = (completed.stdout or "").strip()
        detail = stderr or stdout or f"exit code {completed.returncode}"
        raise CommandError(f"{' '.join(args)} failed: {detail}")
    return completed.stdout if capture_output else ""


def print_json(payload: Any) -> None:
    print(json.dumps(payload, ensure_ascii=False, indent=2))


def strip_ansi(text: str) -> str:
    return ANSI_ESCAPE_RE.sub("", text)


def shorten(text: str, limit: int) -> str:
    compact = " ".join(text.split())
    if len(compact) <= limit:
        return compact
    return compact[: limit - 1].rstrip() + "…"


def unique_strings(values: list[str | None]) -> list[str]:
    seen: set[str] = set()
    results: list[str] = []
    for value in values:
        if not value:
            continue
        cleaned = value.strip()
        if not cleaned:
            continue
        key = cleaned.lower()
        if key in seen:
            continue
        seen.add(key)
        results.append(cleaned)
    return results


def email_localparts(emails: list[str | None]) -> list[str]:
    localparts: list[str | None] = []
    for email in emails:
        if not email or "@" not in email:
            continue
        localparts.append(email.split("@", 1)[0].strip())
    return unique_strings(localparts)


def clean_trace_lines(text: str) -> list[str]:
    lines: list[str] = []
    for raw_line in strip_ansi(text).splitlines():
        line = raw_line.strip()
        if not line:
            continue
        if line.startswith("section_start:") or line.startswith("section_end:"):
            continue
        if line.startswith("$ echo -e "):
            continue
        lines.append(line)
    return lines


def score_trace_line(line: str) -> int:
    score = 0
    lowered = line.lower()

    if SHELL_IF_COMMAND_RE.match(line):
        score -= 80
    elif SHELL_COMMAND_RE.match(line):
        score -= 30

    if GENERIC_JOB_FAILURE_RE.match(line):
        score -= 60

    if ERROR_LINE_RE.search(line):
        score += 30
    if SEMANTIC_FAILURE_RE.search(line) and not SHELL_COMMAND_RE.match(line):
        score += 80
    if "danger" in lowered and ("template" in lowered or "description" in lowered):
        score += 60
    if "generated by danger" in lowered:
        score -= 40
    if "job failed" in lowered and "exit status" in lowered:
        score -= 20
    return score


def extract_failure_excerpt(trace: str, limit: int) -> list[str]:
    lines = clean_trace_lines(trace)
    if not lines:
        return []

    scored_lines = [(index, score_trace_line(line), line) for index, line in enumerate(lines)]
    positive_scored_lines = [item for item in scored_lines if item[1] > 0]
    if not positive_scored_lines:
        source = lines[-max(limit, 12) :]
    else:
        best_index, _, _ = max(positive_scored_lines, key=lambda item: (item[1], item[0]))
        source = []
        for _, score, line in scored_lines[best_index:]:
            if score <= 0 and source:
                break
            if score <= 0 and not source:
                continue
            source.append(line)
            if len(source) >= limit:
                break
        if not source:
            source = [lines[best_index]]

    excerpt: list[str] = []
    seen: set[str] = set()
    for line in source:
        if line in seen:
            continue
        seen.add(line)
        excerpt.append(line)
    return excerpt


def encode_repo(repo: str) -> str:
    return urllib.parse.quote(repo, safe="")


def glab_json(hostname: str, endpoint: str, *, method: str = "GET", fields: dict[str, str] | None = None) -> Any:
    cmd = ["glab", "api", "--hostname", hostname]
    if method != "GET":
        cmd += ["-X", method]
    if fields:
        for key, value in fields.items():
            cmd += ["-f", f"{key}={value}"]
    cmd.append(endpoint)
    output = run_command(cmd)
    if not output.strip():
        return None
    return json.loads(output)


def glab_cli_env(hostname: str) -> dict[str, str]:
    env = os.environ.copy()
    env["GITLAB_HOST"] = hostname
    return env


def extract_mr_web_url(text: str) -> str | None:
    match = MR_WEB_URL_RE.search(text)
    return match.group(0) if match else None


def parse_target_host(target: str) -> dict[str, str | None]:
    """Return hostname and authority (host:port when port is present) from a target."""
    raw = target.strip()
    if not raw:
        raise CommandError("Empty hostname or URL.")
    if "://" in raw:
        parsed = urllib.parse.urlparse(raw)
        if not parsed.hostname:
            raise CommandError(f"Could not parse a hostname from: {raw}")
        authority = f"{parsed.hostname}:{parsed.port}" if parsed.port else None
        return {"hostname": parsed.hostname, "authority": authority}
    cleaned = raw
    if "@" in cleaned:
        cleaned = cleaned.split("@", 1)[1]
    cleaned = cleaned.split("/", 1)[0]
    if ":" in cleaned:
        hostname = cleaned.split(":", 1)[0]
        if not hostname:
            raise CommandError(f"Could not parse a hostname from: {raw}")
        return {"hostname": hostname, "authority": cleaned}
    if not cleaned:
        raise CommandError(f"Could not parse a hostname from: {raw}")
    return {"hostname": cleaned, "authority": None}


def parse_target_hostname(target: str) -> str:
    return parse_target_host(target)["hostname"]


def parse_git_remote(remote: str) -> tuple[str, str]:
    remote = remote.strip()
    if "://" in remote:
        parsed = urllib.parse.urlparse(remote)
        if not parsed.hostname or not parsed.path:
            raise CommandError(f"Unsupported git remote: {remote}")
        host = parsed.hostname
        if parsed.port:
            host = f"{host}:{parsed.port}"
        repo = parsed.path.lstrip("/")
    else:
        cleaned = remote
        if "@" in cleaned:
            cleaned = cleaned.split("@", 1)[1]
        if ":" not in cleaned:
            raise CommandError(f"Unsupported git remote: {remote}")
        host, repo = cleaned.split(":", 1)

    repo = repo.removesuffix(".git").strip("/")
    if not host or not repo:
        raise CommandError(f"Unsupported git remote: {remote}")
    return host, repo


def infer_repo_context() -> tuple[str, str] | None:
    try:
        remote = run_command(["git", "remote", "get-url", "origin"]).strip()
    except CommandError:
        return None
    return parse_git_remote(remote)


def infer_current_branch() -> str | None:
    try:
        branch = run_command(["git", "rev-parse", "--abbrev-ref", "HEAD"]).strip()
    except CommandError:
        return None
    if not branch or branch == "HEAD":
        return None
    return branch


def get_git_email_candidates() -> list[str]:
    candidates: list[str] = []
    for command in (
        ["git", "config", "--get", "--local", "user.email"],
        ["git", "config", "--get", "--worktree", "user.email"],
        ["git", "config", "--get", "user.email"],
        ["git", "config", "--get", "--global", "user.email"],
    ):
        try:
            email = run_command(command).strip()
        except CommandError:
            continue
        if email and email not in candidates:
            candidates.append(email)
    return candidates


def get_authenticated_user(hostname: str) -> dict[str, Any]:
    try:
        user = glab_json(hostname, "user")
    except CommandError:
        return {}
    return user if isinstance(user, dict) else {}


def get_authenticated_username(hostname: str) -> str | None:
    username = get_authenticated_user(hostname).get("username")
    return username.strip() if isinstance(username, str) and username.strip() else None


def resolve_current_user(hostname: str) -> dict[str, Any]:
    email_candidates = get_git_email_candidates()
    localparts = email_localparts(email_candidates)
    authenticated_user = get_authenticated_user(hostname)
    auth_username = (authenticated_user.get("username") or "").strip()
    auth_localparts = email_localparts(
        [
            authenticated_user.get("email"),
            authenticated_user.get("public_email"),
            authenticated_user.get("commit_email"),
        ]
    )

    current_user: str | None = None
    if auth_username:
        auth_username_key = auth_username.lower()
        localpart_keys = [localpart.lower() for localpart in localparts]
        if auth_username_key in localpart_keys or any(
            localpart and auth_username_key.startswith(localpart) for localpart in localpart_keys
        ):
            current_user = auth_username

    if not current_user and localparts:
        current_user = localparts[0]
    if not current_user and auth_username:
        current_user = auth_username
    if not current_user and auth_localparts:
        current_user = auth_localparts[0]

    if not current_user:
        raise CommandError(
            "Could not resolve the current username from git config or the authenticated GitLab account."
        )

    return {
        "username": current_user,
        "candidates": unique_strings([current_user, *localparts, *auth_localparts, auth_username]),
        "git_emails": email_candidates,
        "git_localparts": localparts,
        "authenticated_username": auth_username or None,
    }


def resolve_current_username(hostname: str) -> str:
    return resolve_current_user(hostname)["username"]


def resolve_user_filter(
    value: str | None,
    hostname: str,
    current_user: dict[str, Any] | None,
) -> tuple[str | None, list[str], dict[str, Any] | None]:
    if not value:
        return None, [], current_user
    if value.lower() in MINE_ALIASES:
        current_user = current_user or resolve_current_user(hostname)
        return current_user["username"], current_user["candidates"], current_user
    return value, [value], current_user


def resolve_repo_target(repo: str | None, hostname: str | None) -> dict[str, str]:
    resolved_repo = repo
    resolved_host = hostname
    if not resolved_repo or not resolved_host:
        inferred = infer_repo_context()
        if inferred:
            inferred_host, inferred_repo = inferred
            resolved_repo = resolved_repo or inferred_repo
            resolved_host = resolved_host or inferred_host

    if not resolved_repo:
        raise CommandError("Repository is required when it cannot be inferred from the current checkout.")
    if not resolved_host:
        raise CommandError("Hostname is required when it cannot be inferred from the current checkout.")

    return {
        "hostname": resolved_host,
        "repo": resolved_repo,
        "repo_encoded": encode_repo(resolved_repo),
    }


def resolve_mr_target(target: str, repo: str | None, hostname: str | None) -> dict[str, str]:
    match = MR_URL_RE.match(target)
    if match:
        resolved_host = match.group("host")
        resolved_repo = match.group("repo").strip("/")
        return {
            "hostname": resolved_host,
            "repo": resolved_repo,
            "repo_encoded": encode_repo(resolved_repo),
            "iid": match.group("iid"),
            "mr_url": (
                f"https://{resolved_host}/{resolved_repo}/-/merge_requests/{match.group('iid')}"
                if not match.group("scheme")
                else target.rstrip("/")
            ),
        }

    if not target.isdigit():
        raise CommandError(
            "MR target must be a full merge request URL or a numeric IID with --repo."
        )

    repo_target = resolve_repo_target(repo, hostname)

    return {
        "hostname": repo_target["hostname"],
        "repo": repo_target["repo"],
        "repo_encoded": repo_target["repo_encoded"],
        "iid": target,
        "mr_url": f"https://{repo_target['hostname']}/{repo_target['repo']}/-/merge_requests/{target}",
    }


