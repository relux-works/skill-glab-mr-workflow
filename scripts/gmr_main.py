def command_mr_create(args: argparse.Namespace) -> None:
    ctx = resolve_repo_target(args.repo, args.hostname)
    source_branch = args.source_branch or infer_current_branch()
    if not source_branch:
        raise CommandError(
            "Source branch is required when it cannot be inferred from the current checkout."
        )
    if not args.fill and not args.title:
        raise CommandError("Use --fill or provide --title for non-interactive MR creation.")

    command = [
        "glab",
        "mr",
        "create",
        "-R",
        ctx["repo"],
        "-s",
        source_branch,
        "--yes",
    ]

    if args.fill:
        command.append("--fill")
        if args.fill_commit_body:
            command.append("--fill-commit-body")
    else:
        command += ["-t", args.title, "-d", args.description or ""]

    if args.target_branch:
        command += ["-b", args.target_branch]
    if args.draft:
        command.append("--draft")
    for label in args.label or []:
        command += ["-l", label]
    for reviewer in args.reviewer or []:
        command += ["--reviewer", reviewer]
    for assignee in args.assignee or []:
        command += ["-a", assignee]

    output = run_command(command, env=glab_cli_env(ctx["hostname"]))
    mr_url = extract_mr_web_url(output)
    if not mr_url:
        raise CommandError("Could not determine the created merge request URL from glab output.")
    created_ctx = resolve_mr_target(mr_url, ctx["repo"], ctx["hostname"])
    created_mr = get_mr(created_ctx)
    print_json(
        {
            "action": "create",
            "target": created_ctx,
            "source_branch": source_branch,
            "target_branch": args.target_branch,
            "draft": args.draft,
            "fill": args.fill,
            "mr_url": mr_url,
            "mr": select_mr_fields(created_mr),
            "output": output.strip() or None,
        }
    )


def command_mr_list(args: argparse.Namespace) -> None:
    ctx = resolve_repo_target(args.repo, args.hostname)
    current_user_info: dict[str, Any] | None = None

    if args.draft and args.not_draft:
        raise CommandError("Cannot combine --draft and --not-draft.")

    author, author_candidates, current_user_info = resolve_user_filter(
        args.author,
        ctx["hostname"],
        current_user_info,
    )
    assignee, assignee_candidates, current_user_info = resolve_user_filter(
        args.assignee,
        ctx["hostname"],
        current_user_info,
    )
    reviewer, reviewer_candidates, current_user_info = resolve_user_filter(
        args.reviewer,
        ctx["hostname"],
        current_user_info,
    )

    if args.mine:
        current_user_info = current_user_info or resolve_current_user(ctx["hostname"])
        current_user = current_user_info["username"]
        current_user_candidates = current_user_info["candidates"]
        if args.mine_role == "author":
            if author:
                raise CommandError("Cannot combine --mine with --author.")
            author = current_user
            author_candidates = current_user_candidates
        elif args.mine_role == "assignee":
            if assignee:
                raise CommandError("Cannot combine --mine with --assignee.")
            assignee = current_user
            assignee_candidates = current_user_candidates
        elif args.mine_role == "reviewer":
            if reviewer:
                raise CommandError("Cannot combine --mine with --reviewer.")
            reviewer = current_user
            reviewer_candidates = current_user_candidates

    command = [
        "glab",
        "mr",
        "list",
        "-R",
        ctx["repo"],
        "-F",
        "json",
        "-P",
        str(args.per_page),
    ]

    if args.page != 1:
        command += ["-p", str(args.page)]

    if args.state == "closed":
        command.append("--closed")
    elif args.state == "merged":
        command.append("--merged")
    elif args.state == "all":
        command.append("--all")

    if author:
        command += ["--author", author]
    if assignee:
        command += ["--assignee", assignee]
    if reviewer:
        command += ["--reviewer", reviewer]
    if args.source_branch:
        command += ["--source-branch", args.source_branch]
    if args.target_branch:
        command += ["--target-branch", args.target_branch]
    if args.search:
        command += ["--search", args.search]
    for label in args.label or []:
        command += ["--label", label]
    if args.draft:
        command.append("--draft")
    if args.not_draft:
        command.append("--not-draft")

    output = run_command(command, env=glab_cli_env(ctx["hostname"]))
    merge_requests = post_filter_merge_requests(
        json.loads(output),
        author_candidates=author_candidates,
        assignee_candidates=assignee_candidates,
        reviewer_candidates=reviewer_candidates,
        draft=args.draft,
        not_draft=args.not_draft,
    )

    print_json(
        {
            "target": ctx,
            "filters": {
                "state": args.state,
                "author": author,
                "assignee": assignee,
                "reviewer": reviewer,
                "mine": args.mine,
                "mine_role": args.mine_role if args.mine else None,
                "current_user": current_user_info["username"] if current_user_info else None,
                "current_user_candidates": current_user_info["candidates"] if current_user_info else [],
                "draft": args.draft,
                "not_draft": args.not_draft,
                "source_branch": args.source_branch,
                "target_branch": args.target_branch,
                "search": args.search,
                "labels": args.label or [],
                "page": args.page,
                "per_page": args.per_page,
            },
            "count": len(merge_requests),
            "items": [select_mr_list_fields(mr) for mr in merge_requests],
        }
    )


